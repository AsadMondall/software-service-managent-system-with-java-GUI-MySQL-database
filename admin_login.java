    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
 
        String  uname = uid.getText();
       String Pass = pass.getText();

       try{
         Class.forName("com.mysql.jdbc.Driver");
         Connection conn = DriverManager.getConnection("jdbc:mysql://localhost/scms","root","");
        Statement stmt = conn.createStatement();
        ResultSet rs = stmt.executeQuery(" select * from _admin where admin_name='"+uname+"' and admin_pass='"+Pass+"'");
        if(rs.next())
        {
         dispose();
           
                admin_panel obj = new admin_panel();
                obj.setVisible(true);
        }
            
            
            else {
                JOptionPane.showMessageDialog(null," username or password is incorrect!!!");
            }
        
        conn.close();
       }
       
       catch (Exception e)
       {
           System.out.println(e);
       }
       
    }//GEN-LAST:event_jButton1ActionPerformed

